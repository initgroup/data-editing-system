-- [HOME_RULE_RUN_TREND]
WITH DAYS AS (
    SELECT TRUNC(SYSDATE) - 6 + LEVEL - 1 AS RUN_DAY
      FROM DUAL
   CONNECT BY LEVEL <= 7
),
MENUS AS (
    SELECT 'M03001' AS MENU_CODE, '데이터프로파일링' AS MENU_LABEL FROM DUAL
    UNION ALL
    SELECT 'M03002' AS MENU_CODE, '컬럼간상관분석' AS MENU_LABEL FROM DUAL
    UNION ALL
    SELECT 'M03003' AS MENU_CODE, '자동규칙발굴' AS MENU_LABEL FROM DUAL
    UNION ALL
    SELECT 'M03004' AS MENU_CODE, '규칙위반탐지' AS MENU_LABEL FROM DUAL
    UNION ALL
    SELECT 'M04001' AS MENU_CODE, '통합시나리오실행' AS MENU_LABEL FROM DUAL
),
STATUS_GROUPS AS (
    SELECT 'SUCCESS' AS STATUS_GROUP FROM DUAL
    UNION ALL
    SELECT 'FAILED' AS STATUS_GROUP FROM DUAL
),
COUNTS AS (
    SELECT TRUNC(CAST(R.CREATED_AT AS DATE)) AS RUN_DAY
         , J.MENU_CODE
         , CASE
               WHEN R.STATUS = 'SUCCESS' THEN 'SUCCESS'
               WHEN R.STATUS IN ('FAILED', 'SKIPPED') THEN 'FAILED'
               ELSE 'OTHER'
           END AS STATUS_GROUP
         , COUNT(*) AS CNT
     FROM "INIT$_TB_DATA_WORK_RUN" R
      JOIN "INIT$_TB_DATA_WORK_JOB" J ON J.WORK_JOB_ID = R.WORK_JOB_ID
      JOIN "INIT$_TB_PROJECT" P ON P.PROJECT_ID = J.PROJECT_ID
     WHERE (:includeAllUsers = 'Y' OR P.USER_ID = :userId)
       AND J.MENU_CODE IN ('M03001', 'M03002', 'M03003', 'M03004')
       AND R.CREATED_AT >= TRUNC(SYSDATE) - 6
     GROUP BY TRUNC(CAST(R.CREATED_AT AS DATE)),
              J.MENU_CODE,
              CASE
                  WHEN R.STATUS = 'SUCCESS' THEN 'SUCCESS'
                  WHEN R.STATUS IN ('FAILED', 'SKIPPED') THEN 'FAILED'
                  ELSE 'OTHER'
              END
    UNION ALL
    SELECT TRUNC(CAST(R.CREATED_AT AS DATE)) AS RUN_DAY
         , 'M04001' AS MENU_CODE
         , CASE
               WHEN R.STATUS = 'SUCCESS' THEN 'SUCCESS'
               WHEN R.STATUS IN ('FAILED', 'SKIPPED') THEN 'FAILED'
               ELSE 'OTHER'
           END AS STATUS_GROUP
         , COUNT(*) AS CNT
      FROM "INIT$_TB_FLOW_WORK_RUN" R
      JOIN "INIT$_TB_FLOW_WORK" F ON F.FLOW_ID = R.FLOW_ID
      JOIN "INIT$_TB_PROJECT" P ON P.PROJECT_ID = F.PROJECT_ID
     WHERE (:includeAllUsers = 'Y' OR P.USER_ID = :userId)
       AND F.MENU_CODE = 'M04001'
       AND R.CREATED_AT >= TRUNC(SYSDATE) - 6
     GROUP BY TRUNC(CAST(R.CREATED_AT AS DATE)),
              CASE
                  WHEN R.STATUS = 'SUCCESS' THEN 'SUCCESS'
                  WHEN R.STATUS IN ('FAILED', 'SKIPPED') THEN 'FAILED'
                  ELSE 'OTHER'
              END
)
SELECT TO_CHAR(D.RUN_DAY, 'MM-DD') AS RUN_DATE
     , M.MENU_CODE
     , M.MENU_LABEL
     , S.STATUS_GROUP
     , NVL(C.CNT, 0) AS CNT
  FROM DAYS D
 CROSS JOIN MENUS M
 CROSS JOIN STATUS_GROUPS S
  LEFT JOIN COUNTS C
    ON C.RUN_DAY = D.RUN_DAY
   AND C.MENU_CODE = M.MENU_CODE
   AND C.STATUS_GROUP = S.STATUS_GROUP
 ORDER BY D.RUN_DAY, M.MENU_CODE, S.STATUS_GROUP;

