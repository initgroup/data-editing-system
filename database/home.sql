     , FN.REF_MENU_CODE
     , FN.REF_WORK_JOB_ID
     , J.JOB_GROUP
     , J.JOB_NAME
     , J.JOB_DESC
     , J.EXEC_SOURCE_TYPE
     , J.EXEC_OBJECT_TYPE
     , J.EXEC_OBJECT_NAME
     , J.EXEC_OBJECT_LABEL
     , J.PARAM_JSON AS JOB_PARAM_JSON
     , NR.RUNTIME_PARAM_JSON
     , NR.NODE_PAYLOAD_JSON
     , NR.RUN_OUTPUT_JSON
     , NR.STARTED_AT
     , NR.FINISHED_AT
     , NR.CREATED_AT
     , NR.UPDATED_AT
  FROM "INIT$_TB_FLOW_WORK_NODE_RUN" NR
  JOIN "INIT$_TB_FLOW_WORK_RUN" R ON R.FLOW_RUN_ID = NR.FLOW_RUN_ID
  JOIN "INIT$_TB_FLOW_WORK" F ON F.FLOW_ID = R.FLOW_ID
  JOIN "INIT$_TB_PROJECT" P ON P.PROJECT_ID = F.PROJECT_ID
