        SELECT S.OWNER,
               S.TYPE AS OBJECT_TYPE,
               S.NAME AS OBJECT_NAME,
               MIN(O.CREATED) AS CREATED
          FROM ALL_SOURCE S
          LEFT JOIN ALL_OBJECTS O
            ON O.OWNER = S.OWNER
           AND O.OBJECT_NAME = S.NAME
           AND O.OBJECT_TYPE = S.TYPE
         WHERE S.TYPE IN (/* --OBJECT_TYPE_LIST-- */)
           AND S.NAME NOT LIKE 'BIN$%'
         GROUP BY S.OWNER, S.TYPE, S.NAME
  ) O
 WHERE UPPER(OWNER) LIKE :keyword
    OR UPPER(OBJECT_NAME) LIKE :keyword;

