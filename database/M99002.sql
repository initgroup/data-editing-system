       VIEW_NAME AS OBJECT_NAME,
       VIEW_NAME AS OBJECT_LABEL,
       'VIEW:' || OWNER || ':' || VIEW_NAME AS NODE_ID,
       'GROUP:' || OWNER || ':VIEW' AS PARENT_ID,
       3 AS LEVEL_NO,
       'Y' AS IS_SELECTABLE,
       CAST(NULL AS NUMBER) AS CHILD_COUNT,
       CAST(NULL AS VARCHAR2(4000)) AS OBJECT_COMMENT,
       CAST(NULL AS VARCHAR2(19)) AS CREATED_AT
  FROM ALL_VIEWS
 WHERE OWNER = :owner;

-- [M99002_OBJECT_CHILD_PLSQL]
SELECT S.OWNER,
       S.OBJECT_TYPE,
       S.OBJECT_NAME,
       S.OBJECT_NAME AS OBJECT_LABEL,
       S.OBJECT_TYPE || ':' || S.OWNER || ':' || S.OBJECT_NAME AS NODE_ID,
       'GROUP:' || S.OWNER || ':' || S.OBJECT_TYPE AS PARENT_ID,
       3 AS LEVEL_NO,
       'Y' AS IS_SELECTABLE,
       CASE
           WHEN S.OBJECT_TYPE = 'PACKAGE' THEN (
               SELECT COUNT(*)
                 FROM ALL_PROCEDURES P
                WHERE P.OWNER = S.OWNER
                  AND P.OBJECT_NAME = S.OBJECT_NAME
                  AND P.PROCEDURE_NAME IS NOT NULL
           )
           ELSE CAST(NULL AS NUMBER)
       END AS CHILD_COUNT
  FROM (
        SELECT DISTINCT OWNER, TYPE AS OBJECT_TYPE, NAME AS OBJECT_NAME
          FROM ALL_SOURCE
         WHERE OWNER = :owner
           AND TYPE = :category
           AND NAME NOT LIKE 'BIN$%'
  ) S;

