       TO_CHAR(O.CREATED, 'YYYY-MM-DD HH24:MI:SS') AS CREATED_AT
  FROM ALL_OBJECTS O
 WHERE O.OWNER = :owner
   AND O.OBJECT_NAME = :objectName
   AND O.OBJECT_TYPE = :baseObjectType
   AND O.GENERATED = 'N'
   AND EXISTS (
        SELECT 1
          FROM ALL_SOURCE S
         WHERE S.OWNER = O.OWNER
           AND S.NAME = O.OBJECT_NAME
           AND S.TYPE = O.OBJECT_TYPE
   );

