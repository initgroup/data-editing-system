       OBJECT_TYPE,
       OBJECT_NAME,
       OBJECT_NAME AS OBJECT_LABEL,
       'SEARCH:' || OBJECT_TYPE || ':' || OWNER || ':' || OBJECT_NAME AS NODE_ID,
