    create_index_if_missing('INIT$_IX_NOTICE_ACTIVE', q'[CREATE INDEX "INIT$_IX_NOTICE_ACTIVE" ON "INIT$_TB_NOTICE" ("USE_YN", "POST_START_AT", "POST_END_AT", "PIN_YN", "SORT_ORDER")]');
    create_index_if_missing('INIT$_IX_NOTICE_POPUP', q'[CREATE INDEX "INIT$_IX_NOTICE_POPUP" ON "INIT$_TB_NOTICE" ("POPUP_YN", "POPUP_START_AT", "POPUP_END_AT")]');
    create_index_if_missing('INIT$_IX_SETUP_LOG_CONN', q'[CREATE INDEX "INIT$_IX_SETUP_LOG_CONN" ON "INIT$_TB_SETUP_LOG" ("CONNECTION_ID", "CREATED_AT")]');

    run_ddl(q'[COMMENT ON TABLE "INIT$_TB_USER" IS 'Initial system users for signup and administration']', 'COMMENT INIT$_TB_USER');
    run_ddl(q'[COMMENT ON TABLE "INIT$_TB_DB_CONNECTION" IS 'User-configurable target database connection profiles']', 'COMMENT INIT$_TB_DB_CONNECTION');
    run_ddl(q'[COMMENT ON TABLE "INIT$_TB_SYSTEM_SETTING" IS 'User and target database scoped key-value settings']', 'COMMENT INIT$_TB_SYSTEM_SETTING');
    run_ddl(q'[COMMENT ON TABLE "INIT$_TB_NOTICE" IS 'Administrator notices for dashboard alerts and optional popup display']', 'COMMENT INIT$_TB_NOTICE');
    run_ddl(q'[COMMENT ON TABLE "INIT$_TB_SETUP_LOG" IS 'Initial setup and target schema initialization logs']', 'COMMENT INIT$_TB_SETUP_LOG');
END;
