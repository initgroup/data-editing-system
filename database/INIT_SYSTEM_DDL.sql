    "POPUP_START_AT" TIMESTAMP (6),
    "POPUP_END_AT" TIMESTAMP (6),
    "PIN_YN" CHAR(1 BYTE) DEFAULT 'N' NOT NULL ENABLE,
    "USE_YN" CHAR(1 BYTE) DEFAULT 'Y' NOT NULL ENABLE,
    "SORT_ORDER" NUMBER DEFAULT 0,
