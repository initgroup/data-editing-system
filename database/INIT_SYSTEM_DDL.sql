    create_index_if_missing('INIT$_IX_USER_USE', q'[CREATE INDEX "INIT$_IX_USER_USE" ON "INIT$_TB_USER" ("USE_YN")]');
    create_index_if_missing('INIT$_IX_DB_CONN_DEFAULT', q'[CREATE INDEX "INIT$_IX_DB_CONN_DEFAULT" ON "INIT$_TB_DB_CONNECTION" ("USER_ID", "USE_YN", "DEFAULT_YN", "SORT_ORDER")]');
