    CONSTRAINT "INIT$_FK_DB_CONN_USER" FOREIGN KEY ("USER_ID")
        REFERENCES "INIT$_TB_USER" ("USER_ID") ENABLE,
    CONSTRAINT "INIT$_CK_DB_CONN_DEFAULT" CHECK ("DEFAULT_YN" IN ('Y', 'N')) ENABLE,
    CONSTRAINT "INIT$_CK_DB_CONN_SHARED" CHECK ("SHARED_YN" IN ('Y', 'N')) ENABLE,
    CONSTRAINT "INIT$_CK_DB_CONN_USE" CHECK ("USE_YN" IN ('Y', 'N')) ENABLE
)
]');

    create_table_if_missing('INIT$_TB_SYSTEM_SETTING', q'[
CREATE TABLE "INIT$_TB_SYSTEM_SETTING" (
    "USER_ID" NUMBER NOT NULL ENABLE,
    "CONNECTION_ID" NUMBER NOT NULL ENABLE,
    "CATEGORY_CODE" VARCHAR2(100 BYTE) DEFAULT 'GENERAL' NOT NULL ENABLE,
    "SETTING_KEY" VARCHAR2(150 BYTE) NOT NULL ENABLE,
    "SETTING_VALUE" CLOB,
    "SETTING_DESC" VARCHAR2(1000 BYTE),
    "SORT_ORDER" NUMBER DEFAULT 0,
    "USE_YN" CHAR(1 BYTE) DEFAULT 'Y' NOT NULL ENABLE,
    "UPDATED_AT" TIMESTAMP (6),
    CONSTRAINT "INIT$_PK_SYSTEM_SETTING" PRIMARY KEY ("USER_ID", "CONNECTION_ID", "CATEGORY_CODE", "SETTING_KEY"),
    CONSTRAINT "INIT$_FK_SYSTEM_SETTING_USER" FOREIGN KEY ("USER_ID")
        REFERENCES "INIT$_TB_USER" ("USER_ID") ENABLE,
    CONSTRAINT "INIT$_FK_SYSTEM_SETTING_CONN" FOREIGN KEY ("CONNECTION_ID")
        REFERENCES "INIT$_TB_DB_CONNECTION" ("CONNECTION_ID") ENABLE,
    CONSTRAINT "INIT$_CK_SYSTEM_SETTING_USE" CHECK ("USE_YN" IN ('Y', 'N')) ENABLE
)
]');

    create_table_if_missing('INIT$_TB_NOTICE', q'[
CREATE TABLE "INIT$_TB_NOTICE" (
