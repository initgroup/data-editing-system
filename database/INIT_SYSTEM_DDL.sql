-- [INIT_SYSTEM_DDL]
DECLARE
    FUNCTION object_exists(p_object_name IN VARCHAR2, p_object_type IN VARCHAR2) RETURN BOOLEAN IS
        v_count NUMBER;
    BEGIN
        SELECT COUNT(*)
          INTO v_count
          FROM USER_OBJECTS
         WHERE OBJECT_NAME = UPPER(p_object_name)
           AND OBJECT_TYPE = UPPER(p_object_type);
        RETURN v_count > 0;
    END;

    PROCEDURE run_ddl(p_ddl IN CLOB, p_label IN VARCHAR2) IS
    BEGIN
        EXECUTE IMMEDIATE p_ddl;
        DBMS_OUTPUT.PUT_LINE('[OK] ' || p_label);
    EXCEPTION
        WHEN OTHERS THEN
            DBMS_OUTPUT.PUT_LINE('[ERROR] ' || p_label || ' - ' || SQLERRM);
    END;

    PROCEDURE create_table_if_missing(p_table_name IN VARCHAR2, p_ddl IN CLOB) IS
    BEGIN
        IF object_exists(p_table_name, 'TABLE') THEN
            DBMS_OUTPUT.PUT_LINE('[SKIP] TABLE ' || p_table_name || ' already exists');
        ELSE
            run_ddl(p_ddl, 'CREATE TABLE ' || p_table_name);
        END IF;
    END;

    PROCEDURE create_index_if_missing(p_index_name IN VARCHAR2, p_ddl IN CLOB) IS
    BEGIN
        IF object_exists(p_index_name, 'INDEX') THEN
            DBMS_OUTPUT.PUT_LINE('[SKIP] INDEX ' || p_index_name || ' already exists');
        ELSE
            run_ddl(p_ddl, 'CREATE INDEX ' || p_index_name);
        END IF;
    END;

BEGIN
    create_table_if_missing('INIT$_TB_USER', q'[
CREATE TABLE "INIT$_TB_USER" (
    "USER_ID" NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL ENABLE,
    "LOGIN_ID" VARCHAR2(100 BYTE) NOT NULL ENABLE,
    "USER_NAME" VARCHAR2(200 BYTE) NOT NULL ENABLE,
    "EMAIL" VARCHAR2(300 BYTE) NOT NULL ENABLE,
    "PASSWORD_HASH" VARCHAR2(4000 BYTE),
    "ROLE_CODE" VARCHAR2(30 BYTE) DEFAULT 'USER' NOT NULL ENABLE,
    "USE_YN" CHAR(1 BYTE) DEFAULT 'Y' NOT NULL ENABLE,
    "CREATED_AT" TIMESTAMP (6) DEFAULT SYSTIMESTAMP NOT NULL ENABLE,
    "UPDATED_AT" TIMESTAMP (6),
    CONSTRAINT "INIT$_PK_USER" PRIMARY KEY ("USER_ID"),
    CONSTRAINT "INIT$_UK_USER_LOGIN" UNIQUE ("LOGIN_ID"),
    CONSTRAINT "INIT$_UK_USER_EMAIL" UNIQUE ("EMAIL"),
    CONSTRAINT "INIT$_CK_USER_USE" CHECK ("USE_YN" IN ('Y', 'N')) ENABLE
)
]');

    create_table_if_missing('INIT$_TB_DB_CONNECTION', q'[
